void draw_line(int x0, int y0, int x1, int y1,
        unsigned char v, struct canvas *ca)
{
    if (x0 == x1) {
        draw_vline(x0, y0, y1, v, ca);
        return;
    } else if (y0 == y1) {
        draw_hline(x0, x1, y0, v, ca);
        return;
    }

    int aux;
    if (x0 > x1) {
        aux = x1; x1 = x0; x0 = aux;
        aux = y1; y1 = y0; y0 = aux;
    }

    int dx =  abs(x1-x0);
    int dy = -abs(y1-y0), sy = y0<y1 ? 1 : -1;
    int err = dx+dy, e2;

    long addr;
    unsigned char xx, bb = v << 6;

    for(;;){
        if (x0==x1 && y0==y1) break;
        addr = (x0>>2) + (y0 * ca->rwidth);

        //printf("addr = %d / %l / %d\n", addr, addr, (int) addr);
        
        if (addr >= 0 && addr < ((long) ca->size)) {
            xx = (x0 << 1) & 0x6;
            ca->buf[addr] &= (0xff3f >> xx);
            ca->buf[addr] |= (bb >> xx);

            //set_pixel(x0, y0, v, c);
        }

        e2 = 2*err;
        
        if (e2 >= dy) {
            err += dy;
            x0 ++;
        }
        if (e2 <= dx) {
            err += dx;
            y0 += sy;
        }
    }
}

