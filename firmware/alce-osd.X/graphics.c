void draw_chr2(char c, int x, int y, struct canvas *ca)
{
    unsigned char i, j, b;
    unsigned int idx = (c-0x20) * 5;

#define Y_SCALE 2
#define X_SCALE 1
    for (i = 0; i < 5; i++) {
        c = font1[idx + i];
        for (j = 0; j < 5; j++) {
            b = (c >> (7-j)) & 1;
            if (b) {
                draw_frect(x*X_SCALE + j-1, y + i*Y_SCALE-1, x*X_SCALE + j+X_SCALE, y + i*Y_SCALE+Y_SCALE, 3, ca);
            }
        }
    }
    for (i = 0; i < 5; i++) {
        c = font1[idx + i];
        for (j = 0; j < 5; j++) {
            b = (c >> (7-j)) & 1;
            if (b) {
                //draw_frect(x*X_SCALE + j, y + i*Y_SCALE, x*X_SCALE + j+X_SCALE-1, y + i*Y_SCALE+Y_SCALE-1, 1, ca);
                set_pixel(x + j, y + i*Y_SCALE, b, ca);
                set_pixel(x + j, y + i*Y_SCALE + 1, b, ca);
            }
        }
    }

}

